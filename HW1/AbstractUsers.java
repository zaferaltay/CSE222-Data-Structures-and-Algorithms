/**
 * @author zafer
 *	This class is abstract class for users keeps name and number
 */
public abstract class AbstractUsers {
	private String name;
	private int num;
	

	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
	
}
